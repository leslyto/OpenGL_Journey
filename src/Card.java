
public class Card {
	//private
	private String type;
	private String suit;
	private String value;
	
	//public
	public void setType(String typeln)
	{
		type = typeln;
	}
	public void setSuit(String suitln)
	{
		suit = suitln;
	}
	public void setValue(String valueln)
	{
		value = valueln;
	}
	public String getType()
	{
		return type;
	}
	public String getSuit()
	{
		return suit;
	}
	public String getValue()
	{
		return value;
	}
}
